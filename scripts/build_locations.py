def main():
    url = "https://raw.githubusercontent.com/lutangar/cities.json/master/cities.json"
    print("Fetching cities from", url)
    try:
        with urllib.request.urlopen(url, timeout=60) as r:
            cities = json.load(r)
    except Exception as e:
        print("Fetch failed:", e, "- skipping locations.json")
        return
    out = []
    seen = set()
    for c in cities:
        country = COUNTRY_NAMES.get(c.get("country", ""), c.get("country", ""))
        name = (c.get("name") or "").strip()
        if not name or len(name) < 2:
            continue
        key = name + "|" + country
        if key in seen:
            continue
        seen.add(key)
        out.append([name, country, "city"])
    out_path = Path(__file__).resolve().parent.parent / "static" / "locations.json"
    out_path.parent.mkdir(parents=True, exist_ok=True)
    with open(out_path, "w", encoding="utf-8") as f:
        json.dump(out, f, ensure_ascii=False)
    print("Written", len(out), "locations to", out_path)

if __name__ == "__main__":
    main()
