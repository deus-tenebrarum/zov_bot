async function build() {
  const url = 'https://raw.githubusercontent.com/lutangar/cities.json/master/cities.json';
  console.log('Fetching cities from', url);
  const res = await fetch(url);
  const cities = await res.json();
  const out = [];
  const seen = new Set();
  for (const c of cities) {
    const country = COUNTRY_NAMES[c.country] || c.country;
    const name = (c.name || '').trim();
    if (!name || name.length < 2) continue;
    const key = name + '|' + country;
    if (seen.has(key)) continue;
    seen.add(key);
    const type = 'city';
    out.push([name, country, type]);
  }
  const outPath = path.join(__dirname, '..', 'locations.json');
  fs.writeFileSync(outPath, JSON.stringify(out), 'utf8');
  console.log('Written', out.length, 'locations to', outPath);
}

build().catch(e => { console.error(e); process.exit(1); });
